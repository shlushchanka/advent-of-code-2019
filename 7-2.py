def build_computer_ring(program, std_ins):
  computers = []
  for i in range(len(std_ins)):
    memory = program.copy()
    std_in = std_ins[i]
    std_out = std_ins[(i + 1) % len(std_ins)]
    computers.append(IntCodeComputer(memory, std_in, std_out))
  return computers

def run_circuit(program, phases):
  std_ins = [ [phase] for phase in phases ]
  std_ins[0].append(0)
  computers = build_computer_ring(program, std_ins)

  any_waiting_for_input = True
  while any_waiting_for_input:
    any_waiting_for_input = False
    for c in computers:
      c.run()
      any_waiting_for_input |= c.is_waiting_for_input
      
  return computers[-1].std_out[-1]

def find_max(program, phases):
  from itertools import permutations
  return max([run_circuit(program, permutation) for permutation in permutations(phases)])

